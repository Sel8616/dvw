package cn.sel.dvw.annotation;

import java.lang.annotation.ElementType;
import java.lang.annotation.Retention;
import java.lang.annotation.RetentionPolicy;
import java.lang.annotation.Target;

@Target(ElementType.FIELD)
@Retention(RetentionPolicy.RUNTIME)
public @interface MProperty
{
    /**
     * @return The field's corresponding db column name. "" means as same as the field name.
     */
    String db_col_name() default "";

    /**
     * @return Whether a setter method should be used to set its value. For convenience, 'false' is recommended if there is no special operations on the result.
     */
    boolean useSetter() default false;
}